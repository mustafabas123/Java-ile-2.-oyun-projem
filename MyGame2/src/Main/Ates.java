package Main;

public class Ates {
	
	private int Ates_x;
	private int Ates_y;
	
	
	public Ates(int Ates_x, int Ates_y) {
		this.Ates_x=Ates_x;
		this.Ates_y=Ates_y;
	}

	
	public int getAtesX() {
		return Ates_x;
	}
	public int getAtesY() {
		return Ates_y;
	}
	public void setAtesY(int deger) {
		this.Ates_y=deger;
	}
	public void setAtesX(int deger) {
		this.Ates_x=deger;
	}

}
